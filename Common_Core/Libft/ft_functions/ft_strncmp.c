#include "libft.h"

int	ft_strncmp(char const *s1, char const *s2, size_t n)
{
	unsigned int	i;

	i = 1;
	while (i <= n)
	{
		if (*s1 != *s2)
			return (*s1 - *s2);
		s1 += 1;
		s2 += 1;
		i++;
	}
	return (0);
}
