#include <stdio.h>

char	*ft_strupcase(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 'z')
		{
			str[i] = str[i] - 32;
		}
		i++;
	}
	return (str);
}

int	main(void)
{
	char frase[] = "Esto es una prueba";

	printf("\n%s", frase);
	ft_strupcase(frase);
	printf("\n%s", frase);
}
