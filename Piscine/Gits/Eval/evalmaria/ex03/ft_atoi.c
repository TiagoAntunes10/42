#include <stdio.h>

int	ft_atoi(char *str)
{
	int	result;
	int	sinal;
	int	i;

	result = 0;
	sinal = 1;
	i = 0;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
	{
		i++;
	}
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sinal *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = (result * 10) + (str[i] - 48); 
		i++;
	}
	return (result * sinal);
}

int	main(int argc, char **argv)
{
	(void)argc;
	printf("%i", ft_atoi(argv[1]));
}
